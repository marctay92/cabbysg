package com.example.android.cabbysg;

public class graphvalue {
    int xValue,yValue;

    public graphvalue() {
    }

    public graphvalue(int xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public int getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }
}
